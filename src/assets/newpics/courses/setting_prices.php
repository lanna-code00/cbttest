                            <h2>How can you set prices for your products and services effectively</h2>
                            <p>There are different approaches that businesses use to set their prices. When trying to set prices for your business; Consider the following
                            <li>Your cost</li>
							<li>Understand the common mistakes business owners make in setting their price</li>
							<li>Know the perception of the consumer</li>
							<li>Know what your competitors charge</li>
                            </p>

							<p>
								<h2>Different Pricing methods Includes:</h2>
								<ol>
									<li><b>Cost-based pricing: </b>
										Refers to a pricing method in wichich certain percentage of desired profit margins is added to the cost of the product to obtain the final price.
									</li>
									<li><b>Demanding-based pricing:</b>
										Refers to a pricing method in which the price of a product is finalized according to demand. If the demand is more an organisation prefers to set high prices for product to gain profit.

									</li>
									<li><b>Competition-based pricing: </b>
										Refers to a method in which an organization considers the price of competitors products to set the prizes of its own products
									</li>
									<li><b>Other pricing methods: </b><br>
										<li><b>Value Pricing: </b>
											It implies a method in which an organization tries to win loyal customers by charging low prices their high quality products.
										</li>
										<li><b>Target Return Pricing: </b><br>
											Helps in achieving the required rate of return on investment done for a product
										</li>
									</li>
									<li><b>Going Rate Pricing: </b><br>
										This implies a method in which an organization set the price of a product according to prevailing price trends in the market
									</li>
									<li><b>Transfer Pricing: </b><br>
										This involves selling of goods and services within the departments of the organization.
									</li>
								</ol>
							</p>
							<p>
								<h2>Six different Pricing Strategies: </h2>
								<p><b>Pricing at a Premium: </b> Means pricing a product above normal market value so that customers think a product or service is more valuable than others similar products. It is ver effective in nthe early days of a product life cycle.</p>
								<p><b>Pricing for Market Penetration: </b> this aim to attract buyers by offering lower prices of goods and services. Many companies use it to draw attention away from their competition</p>
								<p><b>Economnic Pricing: </b> aim to attract the most price-consious of consumers. This is very effective to big firms like Wal-Mart but maybe dangerous for small businesses</p>
								<p><b>Price Skimming:</b> Designed to help businesses maximize sales on new products and services. It also involves setting rates high during the introductory phase.</p>
								<p><b>Psychology Pricing: </b> refers to techniques that marketers use to encourage customers to respond on emotional levels rather than lgoical ones</p>
								<p><b>Bundle Pricing: </b>with bundle pricing, small businesses sell multiple products for a lower rate than consumers would face if they purchased each item individually.</p>
							</p>
