                            <h3>Tips for successful survey:</h3>
                            <small><b>Learning about your customers</b></small>
							<ul>
                                <li>Have a clear objective</li>
                                <li>have a clear idea of what you want to get out of the survey</li>
                                <li>make sure your question are not leading the respondents to specific answer</li>
                                <li>Be clear and sue simple language</li>
                                <li>Be concise, long question can be confusing</li>
                                <li>Avoid double barreled question</li>
                                <li>Avoid combining two or more issues in one question</li>
							</ul>
                            <h3>Types of Information to gather in an online survey</h3>
                            <ul>
                                <li>name or nick name</li>
                                <li>Age</li>
