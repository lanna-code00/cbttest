                            <h3>Three steps to follow when identifying your market</h3>
                            <ol>
                                <li><b>Identify why a customer would want to buy your product/service</b><br>
                                <p>
                                    The first step in identifying your target market is to understand what your product/services have to offer to a group of people or businesses. To do this, identify your product or services, features and benefits. A feature is a characteristic of a product/service that automatically comes with it. For example, if toothpaste has a stain-removing formula, that's a feature. The benefit to the customer is whiter teeth. While features are valuable and can certainly enhance your product, benefits motivates people to buy
                                </p>
                                <p>By knowing what your product/service has to offer and what will make customers buy, you can begin to identify common characteristics of your potential market</p>
                                <p><b>For example</b>, there are many different consumers who desire safety as a benefit when purchasing a car. Rather than targeting everyone in their promotional strategy, a car manufacturer may opt to target a specific group of consumers with similar characteristics, such as families with young children. This is an example of market segmentation</p>
                            </li>
                                <li><b>Segment your overall market</b>
                                <br>
                                It is a natural instinct to want to target as many people and groups as possible. However, by doing this your promotional strategy will never talk specifically to any one group, and you will most likely turn many potential customers off. Your promotional budget will be cost effective if target one type of customer and speak directly to them
                                </li>
                                <li><b>Research your market</b><br>

