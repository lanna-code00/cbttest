							<h2>Discussion Questions:</h2>
							<p><b>1</b> Why does Mariam needs a loan from her mother?</p>
							<p><b>Ans</b><br> Mariam has been hand-making tote bags and has received positive feedback and request from her friends to make bags for them. She wants to quit her job which she wasn’t satisfied with and start making and selling bags. However, she doesn’t have the money needed to buy supplies and prepare marketing material. She is asking her mother for the start-up fund needed to begin this business. </p>
							<p><b>2</b> Is Mariam’s mother justified in wanting specific information from her daughter?</p>
							<p><b>Ans</b><br> Yes. She needs to know the expected time for pay back . Also, being able to determine how many  bags Mariam will have to sell will provide her an idea of whether she has a  enough large market for her product.</p>
							<p><b>3</b> Describe Break even point as relates to Mariam’s business Idea. What information will she have to calculate for her breakeven?</p>
							<p><b>Ans</b><br> Breakeven for Mariam would be how many bags she has to sell to cover her expenses.
								<li>She would need to know her cost, the selling price and the unit she expect to sell</li>
							</p>
							<p><b>4</b> How would you describe the three types of cost for Mariam?</p>
							<p><b>Ans</b><br> A cash flow projection uses information to forecast the amount of incoming cash and out going cash your business will incur at different time<br>
								It is used to indicate when your business may be in short of cash and prepare for this in advance by saving more or tap into other funding sources.</p>
								<li><b>A. Start-up cost: </b>
								expenses that happen before the start of a business plan such as:
									<li>Registering for a web domain name</li>
									<li>Paying for a lawyer to structure the business</li>
								</li>
								<li><b>B. Fixed Cost</b>
								expenses for goods and services that do not depend on the number of bags Mariam is making such as:
									<li>Monthly internet access fees</li>
									<li>Insurance</li>
									<li>Wages for the owner</li>
									<li>Rental fees etc</li>
								</li>
								<li><b>C. Variable cost</b>
								cost that change based on the number of bags produced such as:
									<li>Fabrics</li>
									<li>Labels for the bags</li>
									<li>thread</li>
								</li>

							<p><b>5</b> Why does Mariam need to calculate a breakeven for her startup expenses separate from her fixed expenses?</p>
							<p><b>Ans</b><br> 
									<li>To determine how long it would take to recover her startup cost based on profit margins</li>
									<li>It gives her business an easy way to estimate profit and loss</li>
									<li>Any sales above that is profit</li>
									<li>Anything below breakeven is lost</li>
									<li>It could be used to validate the business viability (can you sell enough units of product to make a profit and at what price will you need to sell the product)</li>
									<li>It could also be used to test different pricing strategies, channels of distribution, product design options and business expansion plans.</li>
							</p>
