							<h2>Importance of knowing Features vs. Benefits of a product</h2>
							<p>In sales circles, there is a story often told to highlight features versus benefits. The story goes that there is a man who wants to hang a picture on his wall. In order to do that, he needs to drill a hole in the wall. He goes to his local hardware store to buy a drill. Salesman A shows the man a drill that’s shiny, compact, with 10 different drill bits, and it’s cordless. Salesman B shows the man a drill and says, “This will make a hole in your wall.” The man buys the drill from Salesman B why? </p>
							<p>In this example, the shiny, compact, 10-bit, cordless information focused on features, but failed to tap into what the man really needed.  Salesman B sold the benefit by saying the drill would deliver; a hole.  People buy solutions</p>
                            <p>
                            They have a desire or a problem and they buy the item that fulfills their need. Many business owners make the mistake of thinking buyers will understand what the feature mean in terms of results, and that’s often where their marketing falls short. In the drill example, the man might have thought a shiny compact drill was great, but what he wanted and needed was simply a hole.
                            </p>
