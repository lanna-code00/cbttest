                        <h3>What is a business Sales Forecast?</h3>
                        <p>A business sales forecast is a projection of the amount of sales and potential revenue that you might expect to receive over a definite period of time. While cash flow is the difference between your total income cash and your total outgoing cash</p>

                        <h3>Sales Forecast can be influenced by</h3>
                        <p>
                            <li>Seasonal trends</li>
                            <li>Size of your market</li>
                            <li>Your business history</li>
                            <li>Perceived value of your business’ products and services</li>
                            <li>Major event in your community</li>
                            <li>State of the economy</li>
                            <li>Demands of the market</li>
                            <li>Competition</li>
                            <li>Suppliers</li>
                        </p>
                        <h3>How to gather the right information to forecast sales of your business</h3>
                        <p>
                            <li>Analyzing previous business records</li>
                            <li>Surveying  current and potential customers</li>
                            <li>Research a published consumer data</li>
                            <li>Conducting informational interviews of other business</li>
                            <li>Observing trends in other businesses</li>

                            <p>The method you choose may differ depending on the specific traits of your industry, the amount of time you have operated your business, the unique characteristics of your target audience and the size of your market</p>
                        </p>
